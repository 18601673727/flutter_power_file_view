/// PerviewType
enum PerviewType {
  /// Not initialized
  none,

  /// Unsupported platform
  unsupportedPlatform,

  /// Nonexistent file
  nonExistent,

  /// Unsupported file type
  unsupportedType,

  /// X5 initializing
  engineLoading,

  /// X5 Initialization failed
  engineFail,

  /// Successfully opened file
  done
}
